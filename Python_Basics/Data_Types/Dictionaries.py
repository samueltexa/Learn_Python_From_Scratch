"""Python dictionary is is an unordered sequence of data of key-value pair form.
It is similar to the hash table type. Dictionaries are written within curly
braces in the form key:value. It is very useful to retrieve data in an optimized
way among a large amount of data."""

# Example
#a sample dictionary variable

myDictionary = {1:"first name",2:"last name", "age":33}

#print value having key=1
print(myDictionary[1])
#print value having key=2
print(myDictionary[2])
#print value having key="age"
print(myDictionary["age"])